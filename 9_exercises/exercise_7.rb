#What's the major difference between an Array and a Hash?

=begin

An array is an ordered list of elements, where elements are refrenced by their index. 
A hash consists of key:value pairs, where the key can reference any element. 
Hashes shouldn't be considered ordered, though they are in newer versions of Ruby.

=end

=begin
Book answer:

The major difference between an array and a hash is that a hash contains a key value pair for referencing by key.

=end

